package operadores_relacionais;

public class OperadoresRelacionais {

public static void main(String[] args) {
		
		int a = 15;
		int b = 12;
		
		System.out.println("a = " + a);
		System.out.println("b = " + b);
		System.out.println("a == b: " + (a == b));
		System.out.println("a != b: " + (a != b));
		System.out.println("a < b: " + (a < b));
		System.out.println("a > b: " + (a > b));
		System.out.println("a <= b: " + (a <= b));
		System.out.println("a >= b: " + (a >= b));

	}


}
