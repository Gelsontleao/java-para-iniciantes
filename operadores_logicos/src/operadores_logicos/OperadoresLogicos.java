package operadores_logicos;

public class OperadoresLogicos {

	public static void main(String[] args) {
		int a = 5;
		int b = 2;
		int c = 8;
		
		System.out.println("a > b && a > c: " + (a > b && a > c));
		System.out.println("a < b && a < c: " + (a < b && a < c));
		System.out.println("a > b || a > c: " + (a > b || a > c));
		System.out.println("a < b || a < c: " + (a > b || a > c));

	}

}
